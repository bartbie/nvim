require("alpha").setup(DASH.opts)

---@return string
local function version()
    local s = vim.split(vim.fn.execute("version"), "\n", { trimempty = true })[1]
    s = vim.split(s, " ", { trimempty = true })[2]
    return string.sub(s, 2) -- remove v
end

vim.api.nvim_create_autocmd("User", {
    group = require("bartbie.augroup")("greet_screen"),
    callback = function()
        DASH.section.footer.val = "Neovim " .. version()
        pcall(vim.cmd.AlphaRedraw)
    end,
})
